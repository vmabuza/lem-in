#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	char			*s1;
	unsigned int	count;

	s1 = (char *)s;
	if (s1 && f)
	{
		count = 0;
		while (s1[count])
		{
			(*f)(&s1[count]);
			count++;
		}
	}
}
