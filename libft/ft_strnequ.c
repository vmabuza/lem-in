#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		a;

	if (!n)
		return (1);
	if (s1 && s2 && n)
	{
		a = 0;
		while (n > 0)
		{
			if (s1[a] != s2[a])
				return (0);
			a++;
			n--;
		}
		return (1);
	}
	return (0);
}
