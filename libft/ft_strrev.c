#include "libft.h"

char	*ft_strrev(char *str)
{
	int	i;
	int	j;
	int	temp;

	i = ft_strlen(str);
	j = 0;
	while ((i - 1) > j)
	{
		temp = str[j];
		str[j] = str[i - 1];
		str[i - 1] = temp;
		i--;
		j++;
	}
	return (str);
}
