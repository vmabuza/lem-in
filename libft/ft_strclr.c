void	ft_strclr(char *s)
{
	char			*s1;
	unsigned int	count;

	s1 = (char *)s;
	count = 0;
	if (s1)
	{
		while (s1[count])
		{
			s1[count] = '\0';
			count++;
		}
	}
}
