#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		a;

	a = 0;
	while (src[a])
	{
		dst[a] = src[a];
		a += 1;
	}
	dst[a] = '\0';
	return (dst);
}
